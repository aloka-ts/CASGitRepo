package com.agnity.utility.cdrsftp.dbpush.exceptions;

public class LocationNotFoundException extends Exception {


	/**
	 * 
	 */
	private static final long serialVersionUID = -7103526709563639340L;

	/**
	 * 
	 */
	public LocationNotFoundException() {
		super();
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public LocationNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	/**
	 * @param arg0
	 */
	public LocationNotFoundException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public LocationNotFoundException(Throwable arg0) {
		super(arg0);
	}

}
