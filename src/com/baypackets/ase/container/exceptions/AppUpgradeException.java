package com.baypackets.ase.container.exceptions;

/**
 *
 */
public class AppUpgradeException extends Exception {
	
	/**
	 * 
	 */
	public AppUpgradeException() {
		super();
	}

	/**
	 * 
	 */
	public AppUpgradeException(String message) {
		super(message);
	}

	
	public AppUpgradeException(String message, Throwable cause) {
		super(message, cause);
	}

	public AppUpgradeException(Throwable cause) {
		super(cause);
	}
	
}
