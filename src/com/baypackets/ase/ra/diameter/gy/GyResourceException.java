package com.baypackets.ase.ra.diameter.gy;

import com.baypackets.ase.resource.ResourceException;

public class GyResourceException extends ResourceException {

	public GyResourceException() {
		super();
	}

	public GyResourceException(String message) {
		super(message);
	}

	public GyResourceException(String message, Throwable cause) {
		super(message, cause);
	}

	public GyResourceException(Throwable cause) {
		super(cause);
	}

}
