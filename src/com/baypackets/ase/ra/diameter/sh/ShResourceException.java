package com.baypackets.ase.ra.diameter.sh;


public class ShResourceException extends Throwable {

	public ShResourceException() {
		super();
	}

	public ShResourceException(String message) {
		super(message);
	}

	public ShResourceException(String message, Throwable cause) {
		super(message, cause);
	}

	public ShResourceException(Throwable cause) {
		super(cause);
	}

}
